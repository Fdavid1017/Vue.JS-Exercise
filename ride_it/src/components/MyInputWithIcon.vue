<template>
  <div class="inputWIcon">
    <div class="row">
      <img class="mr-1" :src="iconSrc" alt="icon" />{{
        name
      }}
    </div>
    <div class="row">
      <input
        class="mt-1"
        v-bind:class="hasError ? 'error' : ''"
        ref="ip"
        v-bind:type="type"
        v-bind:name="name"
        :value="variable"
        @input="updateVariable()"
      />
    </div>
  </div>
</template>

<script>
/* eslint-disable space-before-function-paren */

export default {
  props: ['iconSrc', 'type', 'name', 'hasError', 'variable'],
  name: 'MyInput',

  methods: {
    updateVariable() {
      this.$emit('input', this.$refs.ip.value)
    }
  }
}
</script>

